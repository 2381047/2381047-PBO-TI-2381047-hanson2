public class Aritmatika {
    public static void main(String[] args) {
        int operant1 = 20;
        int operant2 = 30;

        System.out.println(operant1 + operant2); // 90
        System.out.println(operant1 - operant2); // 30
        System.out.println(operant1 * operant2); // 1800
        System.out.println(operant1 / operant2); // 2
        System.out.println(operant1 % operant2); // 0 -> Modulo You, Moment ago . uncommited
    }
}
